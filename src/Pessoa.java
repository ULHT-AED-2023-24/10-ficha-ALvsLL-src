class Pessoa {

    String nome;
    String apelido;
    int nrBI;
    String nacionalidade;


    public Pessoa() {
    }


    public Pessoa(String nome, String apelido, int nrBi, String nacionalidade) {
        this.nome = nome;
        this.apelido = apelido;
        this.nrBI = nrBi;
        this.nacionalidade = nacionalidade;
    }

}
